import jsPDF from 'jspdf';
import autoTable from 'jspdf-autotable';

// ==================== TYPES ====================

export type ReportType = 
  | 'executive_summary'
  | 'youth'
  | 'recruiter'
  | 'certificates'
  | 'reviews'
  | 'projects'
  | 'network'
  | 'flagged'
  | 'growth';

export interface ReportFilters {
  status?: 'verified' | 'pending' | 'suspended';
  rating?: number;
  certificateStatus?: 'verified' | 'pending' | 'rejected';
  projectStatus?: 'approved' | 'pending' | 'rejected';
  connectionStatus?: 'accepted' | 'pending' | 'declined';
}

export interface ReportOptions {
  includeCharts?: boolean;
  includeSummary?: boolean;
  customNotes?: string;
  filters?: ReportFilters;
}

export interface DateRange {
  start: Date;
  end: Date;
}

// ==================== HELPER FUNCTIONS ====================

const generateReportId = (): string => {
  return `RPT${Date.now().toString(36).toUpperCase()}${Math.floor(Math.random() * 1000)}`;
};

const formatDate = (date: Date): string => {
  return date.toLocaleDateString('en-US', { 
    year: 'numeric', 
    month: 'long', 
    day: 'numeric' 
  });
};

const formatDateTime = (date: Date): string => {
  return date.toLocaleString('en-US', {
    year: 'numeric',
    month: 'short',
    day: 'numeric',
    hour: '2-digit',
    minute: '2-digit'
  });
};

// Convert chart canvas to image
const chartToImage = async (chartElement: HTMLCanvasElement | null): Promise<string | null> => {
  if (!chartElement) return null;
  try {
    return chartElement.toDataURL('image/png');
  } catch (error) {
    console.error('Error converting chart to image:', error);
    return null;
  }
};

// Add chart placeholder (for future chart integration)
const addChartPlaceholder = (
  doc: jsPDF,
  title: string,
  description: string,
  startY: number
): number => {
  if (startY > 200) {
    doc.addPage();
    startY = 20;
  }
  
  doc.setFontSize(12);
  doc.setFont('helvetica', 'bold');
  doc.text(title, 20, startY);
  
  doc.setFontSize(9);
  doc.setFont('helvetica', 'normal');
  doc.setTextColor(100, 100, 100);
  doc.text(description, 20, startY + 8);
  
  // Placeholder box for chart
  doc.setDrawColor(200, 200, 200);
  doc.setFillColor(250, 250, 250);
  doc.roundedRect(20, startY + 12, 170, 60, 3, 3, 'FD');
  
  doc.setTextColor(150, 150, 150);
  doc.setFontSize(8);
  doc.text('Chart visualization would appear here', 105, startY + 42, { align: 'center' });
  
  return startY + 80;
};

// ==================== COVER PAGE ====================

const addCoverPage = (
  doc: jsPDF,
  reportType: ReportType,
  dateRange: DateRange,
  adminName: string,
  reportId: string
): void => {
  // Background gradient effect (simulated with rectangles)
  doc.setFillColor(59, 130, 246); // Blue
  doc.rect(0, 0, doc.internal.pageSize.getWidth(), 80, 'F');
  
  // Platform logo area (placeholder)
  doc.setTextColor(255, 255, 255);
  doc.setFontSize(24);
  doc.setFont('helvetica', 'bold');
  doc.text('Talent Search Africa', 105, 30, { align: 'center' });
  
  // Report title
  const reportTitles: Record<ReportType, string> = {
    executive_summary: 'Executive Summary Report',
    youth: 'Youth Growth Report',
    recruiter: 'Recruiter Activity Report',
    certificates: 'Certificate Verification Report',
    reviews: 'Reviews & Ratings Report',
    projects: 'Portfolio & Project Report',
    network: 'Network Activity Report',
    flagged: 'Flagged Content & Moderation Report',
    growth: 'Growth & Analytics Report'
  };
  
  doc.setFontSize(20);
  doc.text(reportTitles[reportType] || 'Platform Report', 105, 50, { align: 'center' });
  
  // Date range
  doc.setFontSize(12);
  doc.setFont('helvetica', 'normal');
  doc.text(
    `${formatDate(dateRange.start)} - ${formatDate(dateRange.end)}`,
    105,
    65,
    { align: 'center' }
  );
  
  // Metadata section
  doc.setFillColor(245, 245, 245);
  doc.rect(20, 100, 170, 100, 'F');
  
  doc.setTextColor(0, 0, 0);
  doc.setFontSize(10);
  doc.text(`Generated by: ${adminName}`, 30, 115);
  doc.text(`Generated at: ${formatDateTime(new Date())}`, 30, 125);
  doc.text(`Report ID: ${reportId}`, 30, 135);
  doc.text(`Date Range: ${formatDate(dateRange.start)} to ${formatDate(dateRange.end)}`, 30, 145);
  
  // Footer on cover
  doc.setFontSize(8);
  doc.setTextColor(100, 100, 100);
  doc.text(
    `Generated via Admin Console – #${reportId}`,
    105,
    doc.internal.pageSize.getHeight() - 15,
    { align: 'center' }
  );
  
  doc.addPage();
};

// ==================== EXECUTIVE SUMMARY ====================

const addExecutiveSummary = (
  doc: jsPDF,
  data: any,
  dateRange: DateRange
): number => {
  let yPos = 20;
  
  doc.setFontSize(16);
  doc.setFont('helvetica', 'bold');
  doc.setTextColor(0, 0, 0);
  doc.text('Executive Summary', 20, yPos);
  yPos += 15;
  
  doc.setFontSize(10);
  doc.setFont('helvetica', 'normal');
  doc.setTextColor(60, 60, 60);
  
  const stats = data.stats || {};
  const analytics = data.analytics || {};
  
  // Generate summary text
  const summaryText = [
    `This report provides a comprehensive overview of the Talent Search Africa platform `,
    `for the period from ${formatDate(dateRange.start)} to ${formatDate(dateRange.end)}. `,
    `During this period, the platform has shown significant growth with a total of `,
    `${stats.totalYouth || 0} youth users and ${stats.totalRecruiters || 0} recruiters. `,
    `The platform has processed ${stats.pendingCertificates || 0} certificate verification `,
    `requests, with ${stats.totalProjects || 0} projects uploaded by users. `,
    `A total of ${stats.totalReviews || 0} reviews have been submitted, contributing to `,
    `the platform's reputation system. `,
    ``,
    `Key highlights include:`,
    `• User growth rate: ${analytics.growth?.youthGrowth?.[analytics.growth?.youthGrowth?.length - 1]?.count || 0} total youth users`,
    `• Certificate verification: ${stats.pendingCertificates || 0} pending, ${stats.totalCertificates || 0} total`,
    `• Platform engagement: ${stats.totalProjects || 0} projects, ${stats.totalReviews || 0} reviews`,
    `• Network activity: ${stats.totalConnections || 0} connections established`,
    `• Content moderation: ${stats.pendingReports || 0} reports requiring attention`
  ];
  
  const maxWidth = 170;
  summaryText.forEach((line) => {
    if (line.trim()) {
      const lines = doc.splitTextToSize(line, maxWidth);
      lines.forEach((textLine: string) => {
        if (yPos > 250) {
          doc.addPage();
          yPos = 20;
        }
        doc.text(textLine, 20, yPos);
        yPos += 6;
      });
    } else {
      yPos += 4;
    }
  });
  
  return yPos + 10;
};

// ==================== METRICS CARDS ====================

const addMetricsCards = (
  doc: jsPDF,
  data: any,
  startY: number
): number => {
  let yPos = startY;
  
  doc.setFontSize(14);
  doc.setFont('helvetica', 'bold');
  doc.text('Key Metrics', 20, yPos);
  yPos += 15;
  
  const stats = data.stats || {};
  const metrics = [
    { label: 'Total Youth', value: stats.totalYouth || 0 },
    { label: 'Total Recruiters', value: stats.totalRecruiters || 0 },
    { label: 'Certificates Uploaded', value: stats.totalCertificates || 0 },
    { label: 'Certificates Verified', value: (stats.totalCertificates || 0) - (stats.pendingCertificates || 0) },
    { label: 'Projects Submitted', value: stats.totalProjects || 0 },
    { label: 'Reviews Submitted', value: stats.totalReviews || 0 },
    { label: 'Active Users', value: (stats.totalYouth || 0) + (stats.totalRecruiters || 0) }
  ];
  
  const cardWidth = 85;
  const cardHeight = 30;
  const spacing = 5;
  let xPos = 20;
  
  metrics.forEach((metric, index) => {
    if (index > 0 && index % 2 === 0) {
      xPos = 20;
      yPos += cardHeight + spacing;
    }
    
    if (yPos + cardHeight > 270) {
      doc.addPage();
      yPos = 20;
      xPos = 20;
    }
    
    // Card background
    doc.setFillColor(240, 240, 240);
    doc.roundedRect(xPos, yPos, cardWidth, cardHeight, 3, 3, 'F');
    
    // Card border
    doc.setDrawColor(200, 200, 200);
    doc.roundedRect(xPos, yPos, cardWidth, cardHeight, 3, 3, 'S');
    
    // Metric label
    doc.setFontSize(8);
    doc.setFont('helvetica', 'normal');
    doc.setTextColor(100, 100, 100);
    doc.text(metric.label, xPos + 5, yPos + 8);
    
    // Metric value
    doc.setFontSize(16);
    doc.setFont('helvetica', 'bold');
    doc.setTextColor(59, 130, 246);
    doc.text(metric.value.toString(), xPos + 5, yPos + 20);
    
    xPos += cardWidth + spacing;
  });
  
  return yPos + cardHeight + 15;
};

// ==================== DETAILED TABLES ====================

const addUserTable = (doc: jsPDF, users: any[], startY: number): number => {
  if (users.length === 0) return startY;
  
  doc.setFontSize(14);
  doc.setFont('helvetica', 'bold');
  doc.text('User Details', 20, startY);
  
  const tableData = users.slice(0, 50).map(user => [
    user.userId?.substring(0, 8) || 'N/A',
    user.fullName || 'Unknown',
    user.role || 'N/A',
    user.isVerified ? 'Verified' : 'Pending',
    user.createdAt ? new Date(user.createdAt).toLocaleDateString() : 'N/A',
    user.isSuspended ? 'Suspended' : 'Active'
  ]);
  
  autoTable(doc, {
    startY: startY + 10,
    head: [['UID', 'Name', 'Role', 'Status', 'Joined', 'Last Active']],
    body: tableData,
    styles: { fontSize: 8 },
    headStyles: { fillColor: [59, 130, 246] },
    alternateRowStyles: { fillColor: [245, 245, 245] },
    margin: { left: 20, right: 20 }
  });
  
  return (doc as any).lastAutoTable.finalY + 10;
};

