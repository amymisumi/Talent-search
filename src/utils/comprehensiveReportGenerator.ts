// ==================== ADMIN NOTES ====================

const addAdminNotes = (doc: jsPDF, notes: string, startY: number): number => {
  if (!notes || notes.trim() === '') return startY;
  
  if (startY > 250) {
    doc.addPage();
    startY = 20;
  }
  
  doc.setFontSize(14);
  doc.setFont('helvetica', 'bold');
  doc.text('Admin Notes', 20, startY);
  startY += 10;
  
  doc.setFontSize(10);
  doc.setFont('helvetica', 'normal');
  doc.setTextColor(60, 60, 60);
  
  const maxWidth = 170;
  const lines = doc.splitTextToSize(notes, maxWidth);
  
  lines.forEach((line: string) => {
    if (startY > 270) {
      doc.addPage();
      startY = 20;
    }
    doc.text(line, 20, startY);
    startY += 6;
  });
  
  return startY + 10;
};

// ==================== FOOTER ====================

const addFooter = (doc: jsPDF, reportId: string): void => {
  const pageCount = doc.getNumberOfPages();
  
  for (let i = 1; i <= pageCount; i++) {
    doc.setPage(i);
    doc.setFontSize(8);
    doc.setTextColor(100, 100, 100);
    
    // Page number
    doc.text(
      `Page ${i} of ${pageCount}`,
      doc.internal.pageSize.getWidth() / 2,
      doc.internal.pageSize.getHeight() - 10,
      { align: 'center' }
    );
    
    // Platform name
    doc.text(
      'Talent Search Africa',
      20,
      doc.internal.pageSize.getHeight() - 10
    );
    
    // Report ID
    doc.text(
      `Generated via Admin Console – #${reportId}`,
      doc.internal.pageSize.getWidth() - 20,
      doc.internal.pageSize.getHeight() - 10,
      { align: 'right' }
    );
  }
};

// ==================== MAIN GENERATOR FUNCTION ====================

export const generateComprehensivePDFReport = async (
  reportType: ReportType,
  data: any,
  dateRange: DateRange,
  adminName: string,
  adminEmail: string,
  options: ReportOptions = {}
): Promise<Blob> => {
  const doc = new jsPDF();
  const reportId = generateReportId();
  
  // Cover page
  addCoverPage(doc, reportType, dateRange, adminName, reportId);
  
  let currentY = 20;
  
  // Executive Summary (for all report types)
  if (options.includeSummary !== false) {
    currentY = addExecutiveSummary(doc, data, dateRange);
    
    if (currentY > 250) {
      doc.addPage();
      currentY = 20;
    }
  }
  
  // Key Metrics Cards
  currentY = addMetricsCards(doc, data, currentY);
  
  if (currentY > 250) {
    doc.addPage();
    currentY = 20;
  }
  
  // Charts section (if enabled)
  if (options.includeCharts) {
    const analytics = data.analytics || {};
    
    // User Growth Chart
    if (analytics.growth?.youthGrowth) {
      currentY = addChartPlaceholder(
        doc,
        'User Growth Trend',
        'Shows the growth of youth users over the selected period',
        currentY
      );
    }
    
    // Skills Chart
    if (analytics.skills?.mostDemandedSkills) {
      currentY = addChartPlaceholder(
        doc,
        'Most Demanded Skills',
        'Top skills in demand across the platform',
        currentY
      );
    }
    
    // Reviews Chart
    if (data.reviews || analytics.reviews) {
      currentY = addChartPlaceholder(
        doc,
        'Reviews Breakdown',
        'Distribution of ratings and review activity',
        currentY
      );
    }
  }
  
  // Detailed tables based on report type
  switch (reportType) {
    case 'youth':
      if (data.users) {
        currentY = addUserTable(doc, data.users, currentY);
      }
      break;
    case 'recruiter':
      if (data.recruiters) {
        currentY = addUserTable(doc, data.recruiters, currentY);
      }
      break;
    case 'certificates':
      if (data.certificates) {
        currentY = addCertificateTable(doc, data.certificates, currentY);
      }
      break;
    case 'reviews':
      if (data.reviews) {
        currentY = addReviewTable(doc, data.reviews, currentY);
      }
      break;
    case 'projects':
      if (data.projects) {
        currentY = addProjectTable(doc, data.projects, currentY);
      }
      break;
    case 'network':
      if (data.connections) {
        currentY = addConnectionTable(doc, data.connections, currentY);
      }
      break;
    case 'flagged':
      if (data.reports) {
        currentY = addFlaggedContentTable(doc, data.reports, currentY);
      }
      break;
    case 'executive_summary':
    case 'growth':
      // For executive summary, include all relevant tables
      if (data.users) {
        currentY = addUserTable(doc, data.users.filter((u: any) => u.role === 'youth'), currentY);
        doc.addPage();
        currentY = 20;
      }
      if (data.certificates) {
        currentY = addCertificateTable(doc, data.certificates.slice(0, 30), currentY);
        doc.addPage();
        currentY = 20;
      }
      if (data.reviews) {
        currentY = addReviewTable(doc, data.reviews.slice(0, 30), currentY);
      }
      break;
  }
  
  // Admin Notes
  if (options.customNotes) {
    if (currentY > 250) {
      doc.addPage();
      currentY = 20;
    }
    currentY = addAdminNotes(doc, options.customNotes, currentY);
  }
  
  // Add footer to all pages
  addFooter(doc, reportId);
  
  return doc.output('blob');
};

